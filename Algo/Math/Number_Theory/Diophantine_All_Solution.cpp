struct Diophantine {
    struct num {
        ll x, y, g;
        num(ll x, ll y, ll gcd) : x(x), y(y), g(gcd) {}
    };

    num ext_gcd(ll a, ll b) {
        if (b == 0) return num(1, 0, a);
        num prev = ext_gcd(b, a % b);
        ll x = prev.y,y = prev.x - (a / b) * prev.y;
        return num(x, y, prev.g);
    }

    vector<pair<ll, ll>> solution(ll a, ll b, ll c, ll left, ll right) {
        vector<pair<ll, ll>> ans;
        num res = ext_gcd(a, b);

        if(c % res.g) return ans;

        ll x0 = res.x * (c / res.g), y0 = res.y * (c / res.g);
        ll p = b / res.g, q = a / res.g;

        for(ll k = 0, x, y; ; k++){
            x = x0 + p * k;
            y = y0 - q * k;

            if(min(x, y) < left || max(x, y) > right)
                break;

            ans.pb({x, y});
        }

        return ans;
    }
} diop;

