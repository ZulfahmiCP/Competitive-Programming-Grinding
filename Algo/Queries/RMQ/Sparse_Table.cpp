template<typename T, bool maxi = false>
struct RMQ {
    int N, LOG;
    vector<vector<T>> tab;
    vector<T> arr, logs;

    RMQ(int n = 0) : N(n), LOG(log2(N) + 1), tab(N, vector<T>(LOG)), logs(N + 1, 0), arr(N) {}
    
    void build() {
        for(int i = 2; i <= N; i++)
            logs[i] = 1 + logs[i >> 1];

        for(int i = 0; i < N; i++)
            tab[i][0] = arr[i];

        for(int j = 1; j < LOG; j++)
            for(int i = 0; i + (1 << j) <= N; i++)
                tab[i][j] = best(tab[i][j - 1], tab[i + (1 << (j - 1))][j - 1]);
    }

    T best(T &a, T &b) {
        return maxi ? max(a, b) : min(a, b);
    }

    T calc(int l, int r){
        int k = logs[r - l + 1];
        return best(tab[l][k], tab[r - (1 << k) + 1][k]);
    }
};

